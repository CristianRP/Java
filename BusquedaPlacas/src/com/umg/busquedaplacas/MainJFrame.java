    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //HandlerFile.getInstance().searchPlaca(jTextField1.getText());
        String msg_encontrada = "Placa %1$s encontrada!";
        String msg_placa_anterior = "Placa anterior: %1$s";
        String msg_placa_posterior = "Placa posterior: %1$s";
        boolean encontrado = false;
        for (int i = 0; i < placas.size(); i++) {
            if (placas.get(i).equals(jTextField1.getText())) {
                encontrado = true;
                jLabel6.setForeground(Color.GREEN);
                jLabel6.setText(String.format(Locale.getDefault(), msg_encontrada, placas.get(i)));
                if (placas.indexOf(placas.get(i)) == 0) {
                    jLabel3.setText("Este es el primer registro!");
                } else {
                    jLabel3.setText(String.format(Locale.getDefault(), msg_placa_anterior, placas.get(i - 1)));
                }
                if (placas.indexOf(placas.get(i)) != placas.size() - 1) {
                    jLabel4.setText(String.format(Locale.getDefault(), msg_placa_posterior, placas.get(i + 1)));
                } else {
                    jLabel4.setText("Este es el Ãºltimo registro!");
                }
            } 
        }
        jLabel3.setVisible(encontrado);
        jLabel4.setVisible(encontrado);
        if (!encontrado) {
            jLabel6.setForeground(Color.red);
            jLabel6.setText("Esta placa no existe en el registro!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

